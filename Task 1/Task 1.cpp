#include <iostream>

int main() {
	setlocale(LC_ALL, "Russian");
    int utensils[2][6] = {
        {3, 3, 3, 3, 3, 4}, // Первый ряд
        {3, 3, 3, 3, 3, 4}  // Второй ряд
    };

    int plates[2][6] = {
        {2, 2, 2, 2, 2, 3}, // Первый ряд
        {2, 2, 2, 2, 2, 3}  // Второй ряд
    };

    int chairs[2][6] = {
        {1, 1, 1, 1, 1, 1}, // Первый ряд
        {1, 1, 1, 1, 1, 1}  // Второй ряд
    };

    // События на банкете
    // 1. На пятое место первого ряда приставили ещё один стул
    chairs[0][4] += 1;

    // 2. С третьего места второго ряда украли ложку
    utensils[1][2] -= 1;

    // 3. VIP-гость поделился ложкой с гостем, который остался без неё
    utensils[1][2] += 1; // Возвращаем ложку на место
    utensils[0][5] -= 1; // VIP-гость использует десертную ложку вместо обычной

    // 4. У VIP-гостя забрали десертную тарелку
    plates[0][5] -= 1;

    // Вывод итоговых массивов
    std::cout << "Утварь (вилка, ложка, нож):\n";
    for (int i = 0; i < 2; ++i) {
        for (int j = 0; j < 6; ++j) {
            std::cout << utensils[i][j] << " ";
        }
        std::cout << std::endl;
    }

    std::cout << "\nТарелки:\n";
    for (int i = 0; i < 2; ++i) {
        for (int j = 0; j < 6; ++j) {
            std::cout << plates[i][j] << " ";
        }
        std::cout << std::endl;
    }

    std::cout << "\nСтулья:\n";
    for (int i = 0; i < 2; ++i) {
        for (int j = 0; j < 6; ++j) {
            std::cout << chairs[i][j] << " ";
        }
        std::cout << std::endl;
    }

    return 0;
}